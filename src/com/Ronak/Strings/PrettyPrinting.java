package com.ronak.Strings;

public class PrettyPrinting {

    public static void main(String[] args) {

        float f = 453.5678f;
        System.out.printf("formatted number is %.2f\n", f);

        System.out.println(Math.PI);

        System.out.printf("Pie %.3f\n", Math.PI);

        System.out.printf("Hello my name is %s and I am %s", "Ronak", "cool");

    }

}
